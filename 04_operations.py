# Operator Priorities
# 1. prenthesis ()
# 2. exponentiation **
# 3. mul / div
# 4. add / sub

x = 0

x = x + 10
print(f"x = x + 10 -> {x}")
x += x
print(f"x += x -> {x}")

print(10 - 1)
print(10 + 1)

print(10 * 5)
print(10 / 3)
print(10 // 3) # int of division
print(10 % 3) # remainder of division

print(2 ** 2) # exponentiation

print(abs(-2))


